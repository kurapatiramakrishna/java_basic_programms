import java.util.Scanner;
class Voluem_of_a_cylinder1
{
	public static void main(String[] args) 
	{
		Scanner scan=new Scanner(System.in);
		System.out.println("To calculate the voluem of a cylinder is:");
		System.out.println("Enter the voluem is:");
		Double r=scan.nextDouble();
		System.out.println("Enter the height:");
		Double h=scan.nextDouble();
		Double voluem=(3.14*r*r*h);
		System.out.println("The voluem of a cylinder is:"+voluem);
		}
}
